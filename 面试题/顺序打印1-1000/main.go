package main

import "fmt"

func main() {
	// 创建无缓冲信号通道（空结构体占0内存，仅用于协程同步）
	// 作用：子协程打印完成后，向主协程发送"完成信号"
	s := make(chan struct{})

	// 创建map，存储100个协程对应的数字接收通道
	// key：协程编号(1-100)，value：对应协程接收数字的通道
	mp := make(map[int]chan int, 100)
	// 循环初始化100个协程的数字接收通道
	for i := 1; i <= 100; i++ {
		mp[i] = make(chan int) // 每个协程分配一个无缓冲通道
	}

	// 循环启动100个协程
	for i := 1; i <= 100; i++ {
		// 闭包传参：将当前循环的i（协程编号）传入协程，避免循环变量引用问题
		go func(id int) {
			// 无限循环：持续监听当前协程的数字接收通道
			for {
				// 从当前协程的通道中接收数字（阻塞，直到主协程发送数字）
				fmt.Println(<-mp[id])
				// 向主协程发送"打印完成"信号，解除主协程的阻塞
				s <- struct{}{}
			}
		}(i) // 把循环变量i作为参数传给协程函数
	}

	// 主协程循环分发1-1000的数字到对应协程
	for i := 1; i <= 1000; i++ {
		// 计算当前数字应分配给哪个协程：
		// 规则：数字%100=0 → 分配给100号协程；否则分配给 数字%100 号协程
		id := i % 100
		if id == 0 {
			id = 100
		}

		// 向目标协程的通道发送数字（阻塞，直到协程接收数字）
		mp[id] <- i
		// 等待目标协程的"打印完成"信号（保证数字按1-1000顺序打印）
		<-s
	}
}